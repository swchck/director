func TestOptions_CustomPollInterval(t *testing.T) {
	store := newMockStorage()
	notif := newMockNotifier()
	reg := newMockRegistry()

	// Custom options should not cause construction to fail.
	mgr := manager.New(store, notif, reg, manager.Options{
		PollInterval:             30 * time.Second,
		HeartbeatInterval:        5 * time.Second,
		WaitConfirmationsTimeout: 10 * time.Second,
		WSPollInterval:           1 * time.Minute,
		WSDebounce:               500 * time.Millisecond,
		AdvisoryLockKey:          42,
		ServiceName:              "test",
	})

	if mgr == nil {
		t.Fatal("New with custom options returned nil")
	}
}

