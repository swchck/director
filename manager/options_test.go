func TestOptions_WithInstanceID(t *testing.T) {
	store := newMockStorage()
	notif := newMockNotifier()
	reg := newMockRegistry()

	mgr := manager.New(store, notif, reg, manager.Options{ServiceName: "test"},
		manager.WithInstanceID("my-instance"),
	)

	if mgr.InstanceID() != "my-instance" {
		t.Errorf("InstanceID = %q, want 'my-instance'", mgr.InstanceID())
	}
}

func TestOptions_AutoGeneratedInstanceID(t *testing.T) {
	store := newMockStorage()
	notif := newMockNotifier()
	reg := newMockRegistry()

	mgr := manager.New(store, notif, reg, manager.Options{ServiceName: "test"})

	id := mgr.InstanceID()
	if id == "" {
		t.Error("InstanceID should be auto-generated, got empty string")
	}

	// Each manager should get a unique ID.
	mgr2 := manager.New(store, notif, reg, manager.Options{ServiceName: "test"})
	if mgr2.InstanceID() == id {
		t.Error("Two managers should have different auto-generated IDs")
	}
}

